'''
Exercício Python 006: Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.
'''

input_num = float(input("Olá usuário, insira um número real!\n"))

print(f"O dobro do número inserido é {2*input_num}")

print(f"O triplo do número inserido é {3*input_num}")

print(f"A raiz quadrada do número inserido é {input_num**(0.5):.2f}")

print("Obrigado!")
