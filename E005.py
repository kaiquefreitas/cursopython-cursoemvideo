'''
Exercício Python 005: Faça um programa que leia um número Inteiro e 
mostre na tela o seu sucessor e seu antecessor.
'''

input_num = int(input("Olá usuário, insira um número. \n"))


print(f"O antecessor do número inserido é: {input_num-1}")

print(f"O seu sucessor é: {input_num+1}")

print("Obrigado!")
